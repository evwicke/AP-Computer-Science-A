public class WhileDemo{
    public static void main(String[] args){
        
        /*
         * Types of loops:
         * // "pre conditional loop"
         * while
         * 
         * //will always run at least 1 time. "Post-conditional loop"
         * do{
         *     statements;
         * }while(condition);
         * 
         * for // counter - controlled.
         * for each 
         * 
         * 
         */
        
        int counter1 = 1;
        while(counter1<10){
            
            System.out.print(counter1 + "\t");
            
            
            counter1++;
        }
        
        
        
        
    }
}
